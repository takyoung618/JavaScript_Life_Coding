// 부품화
// 메소드는 부품화의 예라고 할 수 있다. 

// 은닉화, 캡슐화
// 제대로된 부품이라면 그것의 제작과정을 몰라도 사용하는 방법만 알면 쓸 수 있어야 한다. (컴퓨터와 모니터)
// 즉 내부 동작 방법을 단단한 케이스 안으로 숨기고 사용자에게는 그 부품의 사용방법만을 노출하고 있는 것이다.

// 인터페이스
// 잘 만들어진 부품이라면 부품과 부품을 서로 교환할 수 있어야 한다.
// ex) A사의 모니터 대신에 B사의 모니터를 컴퓨터에 연결가능하다
//     -> 모니터와 컴퓨터를 연결하는 케이블이 규격이 표준화 되어 있기 때문에. 